#반지름을 입력받아서 저장
pizza_r = int(input("피자의 반지름을 입력하시오. : "))
PI = 3.14 # 3.14는 상수 -> 대문자로 변수 생성.

#피자의 면적을 계산
pizza = PI * pizza_r ** 2

#피자의 면적을 출력
print("반지름이 {} 인 피자의 면적은 {:.2f} 입니다.".format(pizza_r, pizza))
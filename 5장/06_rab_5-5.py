num = int(input("정수를 입력하세요. : "))

fact = 1 #팩토리얼에 1을 지정
for i in range(1, num + 1) : #반복문으로 1 ~ 입력받은 정수 + 1까지 반복선언
    fact = fact * i #팩토리얼에 i를 곱한다
    
print(f"{num} !은 {fact} 이다") #곱하고 반복을 반복한 값을 출력